//Function to reverse the queue.
class Solution
{
    public:
    queue<int> rev(queue<int> q)
    {
        vector<int>vec;
        queue<int>q1;
        while(!q.empty())
        {
            vec.push_back(q.front());
            q.pop();
        }
        reverse(vec.begin(),vec.end());
        for(int i=0; i<vec.size(); i++)
        {
            q1.push(vec[i]);
        }
        return q1;
    }
};


